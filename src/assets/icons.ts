export const getIconByName = (name: keyof typeof ICON_CATEGORIES.navigation | keyof typeof ICON_CATEGORIES.quiz | keyof typeof ICON_CATEGORIES.ui | keyof typeof ICON_CATEGORIES.status | keyof typeof ICON_CATEGORIES.communication | keyof typeof ICON_CATEGORIES.data | keyof typeof ICON_CATEGORIES.social | keyof typeof ICON_CATEGORIES.system | keyof typeof ICON_CATEGORIES.time | keyof typeof ICON_CATEGORIES.location | keyof typeof ICON_CATEGORIES.media | keyof typeof ICON_CATEGORIES.development | keyof typeof ICON_CATEGORIES.business | keyof typeof ICON_CATEGORIES.education | keyof typeof ICON_CATEGORIES.health | keyof typeof ICON_CATEGORIES.entertainment | keyof typeof ICON_CATEGORIES.weather | keyof typeof ICON_CATEGORIES.food | keyof typeof ICON_CATEGORIES.transport | keyof typeof ICON_CATEGORIES.technology | keyof typeof ICON_CATEGORIES.security | keyof typeof ICON_CATEGORIES.utility | keyof typeof ICON_CATEGORIES.file | keyof typeof ICON_CATEGORIES.arrow | keyof typeof ICON_CATEGORIES.math | keyof typeof ICON_CATEGORIES.other) => {
  // Search through all categories to find the icon
  for (const category of Object.values(ICON_CATEGORIES)) {
    if (name in category) {
      return category[name as keyof typeof category];
    }
  }
  return null;
};

export const getIconByCategory = (category: keyof typeof ICON_CATEGORIES) => {
  return ICON_CATEGORIES[category];
};

